CREATE TYPE section_key AS ENUM (
  'external_approach',
  'grounds',
  'bin_store',
  'car_park',
  'external_fabric',
  'roof',
  'communal_entrance',
  'stairwells',
  'lifts',
  'plant_room',
  'internal_communal',
  'additional'
