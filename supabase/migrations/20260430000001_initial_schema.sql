CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ─── Section key enum ────────────────────────────────────────────────────────
-- template_order: external_approach=1, grounds=2, bin_store=3, car_park=4,
--   external_fabric=5, roof=6, communal_entrance=7, stairwells=8,
