-- ─── User profiles ───────────────────────────────────────────────────────────
-- Extends auth.users. Created automatically by trigger on signup.
CREATE TABLE public.users (
  id          UUID        PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  full_name   TEXT        NOT NULL,
  email       TEXT        NOT NULL,
  role        TEXT        NOT NULL DEFAULT 'inspector'
                          CHECK (role IN ('inspector', 'admin')),
  created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Auto-create profile when a new auth user is created.
-- Caller must pass full_name in raw_user_meta_data when inviting/creating users.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  INSERT INTO public.users (id, full_name, email)
  VALUES (
    NEW.id,
    COALESCE(NEW.raw_user_meta_data->>'full_name', split_part(NEW.email, '@', 1)),
    NEW.email
  );
  RETURN NEW;
END;
$$;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- ─── Properties ──────────────────────────────────────────────────────────────
-- Seeded from CSV. Never auto-generated by the app.
CREATE TABLE public.properties (
  id                 UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  ref                TEXT        NOT NULL UNIQUE,
  name               TEXT        NOT NULL,
  management_company TEXT        NOT NULL,
  block_type         TEXT        NOT NULL,
  address            TEXT        NOT NULL,
  number_of_units    INTEGER     NOT NULL,
  manager_name       TEXT        NOT NULL,  -- matches users.full_name
  has_car_park       BOOLEAN     NOT NULL DEFAULT false,
  has_lift           BOOLEAN     NOT NULL DEFAULT false,
  has_roof_access    BOOLEAN     NOT NULL DEFAULT false,
  created_at         TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- ─── Inspections ─────────────────────────────────────────────────────────────
CREATE TABLE public.inspections (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  property_id      UUID        NOT NULL REFERENCES public.properties(id),
  inspector_id     UUID        NOT NULL REFERENCES public.users(id),
  status           TEXT        NOT NULL DEFAULT 'active'
                               CHECK (status IN ('active', 'completed', 'report_generated')),
  start_time       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  end_time         TIMESTAMPTZ,
  weather          TEXT,
  next_inspection  TEXT,
  report_docx_url  TEXT,
  report_pdf_url   TEXT,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- ─── Observations ────────────────────────────────────────────────────────────
CREATE TABLE public.observations (
  id                  UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  inspection_id       UUID        NOT NULL REFERENCES public.inspections(id) ON DELETE CASCADE,
  property_id         UUID        NOT NULL REFERENCES public.properties(id),
  section_key         section_key NOT NULL,
  template_order      INTEGER     NOT NULL,
  raw_narration       TEXT,
  processed_text      TEXT,
  action_text         TEXT,
  risk_level          TEXT        CHECK (risk_level IN ('High', 'Medium', 'Low')),
  responsibility      TEXT,
  classification_conf TEXT        CHECK (classification_conf IN ('auto', 'manual')),
  created_at          TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- ─── Photos ──────────────────────────────────────────────────────────────────
CREATE TABLE public.photos (
  id               UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  observation_id   UUID        NOT NULL REFERENCES public.observations(id) ON DELETE CASCADE,
  inspection_id    UUID        NOT NULL REFERENCES public.inspections(id) ON DELETE CASCADE,
  storage_path     TEXT,
  local_path       TEXT,
  caption          TEXT,
  opus_description JSONB,
  created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

