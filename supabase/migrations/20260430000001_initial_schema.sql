-- users: own profile only
CREATE POLICY "users_select_own" ON public.users
  FOR SELECT USING (auth.uid() = id);

CREATE POLICY "users_update_own" ON public.users
  FOR UPDATE USING (auth.uid() = id);

-- properties: inspectors see properties where manager_name = their full_name;
--             admins see all
CREATE POLICY "properties_select" ON public.properties
  FOR SELECT USING (
    EXISTS (
      SELECT 1 FROM public.users u
      WHERE u.id = auth.uid()
        AND (u.role = 'admin' OR u.full_name = properties.manager_name)
    )
  );

-- admins can update property flags (has_car_park etc.)
CREATE POLICY "properties_update_admin" ON public.properties
  FOR UPDATE USING (
    EXISTS (SELECT 1 FROM public.users u WHERE u.id = auth.uid() AND u.role = 'admin')
  );

-- inspections: own records only
CREATE POLICY "inspections_all_own" ON public.inspections
  FOR ALL USING (inspector_id = auth.uid());

-- observations: accessible when the parent inspection belongs to the current user
CREATE POLICY "observations_all_own" ON public.observations
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM public.inspections i
      WHERE i.id = inspection_id AND i.inspector_id = auth.uid()
    )
  );

-- photos: same as observations
CREATE POLICY "photos_all_own" ON public.photos
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM public.inspections i
      WHERE i.id = inspection_id AND i.inspector_id = auth.uid()
    )
  );

